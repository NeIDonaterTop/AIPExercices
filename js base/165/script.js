// Задание 1
// Недостатки:
// 1. Название функции "sum" может вводить в заблуждение, так как возвращает не сумму, а среднее значение.
// 2. Необходимо использовать let для объявления переменной res.
// 3. При делении на ноль (если arr.length === 0) будет возвращено NaN.
// 4. Название переменной res не очень информативное.
function calculateAverage(arr) {
  if (arr.length === 0) {
    return 0; 
  }

  let sum = 0;

  for (let elem of arr) {
    sum += elem;
  }

  return sum / arr.length;
}

// Задание 2
// Недостатки:
// 1. Названия параметров arr1 и arr2 не используются в теле функции.
// 2. Названия переменных res1 и res2 могут вводить в заблуждение, так как обе они используют один и тот же arr.
function calculateRatio(arr1, arr2) {
  let sum1 = 0;
  let sum2 = 0;

  for (let elem of arr1) {
    sum1 += elem;
  }

  for (let elem of arr2) {
    sum2 += elem;
  }

  return sum1 / sum2;
}

// Задание 3
// Недостатки:
// 1. Используется оператор *= вместо += для накопления суммы, что приведет к нулевому результату при любых значениях массива.
// 2. Название функции "getSum" может вводить в заблуждение, так как возвращает произведение, а не сумму.
// 3. Необходимо использовать let для объявления переменной res.
function calculateProduct(arr) {
  let res = 1;

  for (let elem of arr) {
    res *= elem;
  }

  return res;
}
